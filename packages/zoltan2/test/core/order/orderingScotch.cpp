  cmdp.setOption("outputFile", &outputFile,
                 "Name of file to write the permutation");
  cmdp.setOption("verbose", "quiet", &verbose,
                 "Print messages and results.");

  if (rank == 0 ) {
    std::cout << "Starting everything" << std::endl;
  }

  //////////////////////////////////
