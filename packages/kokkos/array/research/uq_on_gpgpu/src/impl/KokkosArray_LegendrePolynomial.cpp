void gauss_legendre( unsigned n , double pt[] , double wgt[] )
{
  if ( 32 == n ) {
    for ( unsigned i = 0 ; i < n ; ++i ) {
      pt[i] = points_n32[i] ;
      wgt[i] = weights_n32[i] ;
    }
  }
  else if ( n <= 20 ) {
    for ( unsigned i = 0 ; i < n ; ++i ) {
      pt[i] = points[n][i] ;
      wgt[i] = weights[n][i] ;
    }
  }
  else {
    std::ostringstream msg ;
    msg << "KokkosArray::Impl::gauss_legendre( " << n << " > maximum(20) )" ;
    throw std::runtime_error( msg.str() );
  }

}

